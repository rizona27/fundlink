    final pct = (ratio * 100).round();
    return Padding(
      padding: const EdgeInsets.only(bottom: 4),
      child: Row(children: [
        SizedBox(width: 32, child: Text(label, style: TextStyle(fontSize: 12, color: subTextColor))),
